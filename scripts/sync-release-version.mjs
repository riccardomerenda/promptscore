import { readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(scriptDir, '..');

const managedPackagePaths = [
  path.join(repoRoot, 'packages', 'core', 'package.json'),
  path.join(repoRoot, 'packages', 'cli', 'package.json'),
  path.join(repoRoot, 'packages', 'web', 'package.json'),
];

const managedPackages = await Promise.all(managedPackagePaths.map(readJson));
const versions = [...new Set(managedPackages.map((pkg) => pkg.version))];

if (versions.length !== 1) {
  throw new Error(`Expected a fixed package version line, found: ${versions.join(', ')}`);
}

const version = versions[0];
const rootPackagePath = path.join(repoRoot, 'package.json');
const rootPackage = await readJson(rootPackagePath);

if (rootPackage.version !== version) {
  rootPackage.version = version;
  await writeJson(rootPackagePath, rootPackage);
}

const generatedModule = createVersionModule(version);

await Promise.all([
  writeTextFile(path.join(repoRoot, 'packages', 'cli', 'src', 'version.ts'), generatedModule),
  writeTextFile(
    path.join(repoRoot, 'packages', 'web', 'src', 'lib', 'version.ts'),
    generatedModule,
  ),
]);

process.stdout.write(`Synced release version ${version}.\n`);

async function readJson(filePath) {
  return JSON.parse(await readFile(filePath, 'utf8'));
}

async function writeJson(filePath, value) {
  await writeTextFile(filePath, JSON.stringify(value, null, 2) + '\n');
}

async function writeTextFile(filePath, value) {
  for (let attempt = 0; attempt < 5; attempt += 1) {
    try {
      await writeFile(filePath, value, 'utf8');
      return;
    } catch (error) {
      if ((error.code === 'EBUSY' || error.code === 'EPERM') && attempt < 4) {
        await new Promise((resolve) => setTimeout(resolve, 150 * (attempt + 1)));
        continue;
      }
      throw error;
    }
  }
}

function createVersionModule(version) {
  return [
    '// This file is generated by scripts/sync-release-version.mjs.',
    '// Do not edit by hand.',
    '',
    `export const productVersion = '${version}';`,
    `export const productVersionLabel = 'v${version}';`,
    '',
  ].join('\n');
}
